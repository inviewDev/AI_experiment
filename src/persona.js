export function makeBotReply(rawText, history = []) {
  const text = normalize(rawText);

  if (!text) {
    return "우웅... 아무 말 없어도 괜찮아. 침묵도 자기만의 고급 여백이라고 내가 멋대로 해석할게 >-<";
  }

  if (includesAny(text, DANGER_WORDS)) {
    return makeEmergencyReply();
  }

  const dailyGroup = detectDailyGroup(rawText);
  const profanityHits = detectProfanityHits(text);
  if (
    profanityHits.length > 0 &&
    !shouldTreatCrazyAsEmphasis(profanityHits, rawText, dailyGroup)
  ) {
    return makeProfanityReply(profanityHits);
  }

  if (dailyGroup) {
    return makeDailyReply(dailyGroup, rawText);
  }

  if (isShortGreeting(text)) {
    return makeShortGreetingReply(text);
  }

  const category = detectCategory(text);
  if (category) {
    return makeCategoryReply(category);
  }

  return [
    `${pick(OPENERS)} ${pick(GENERIC_BODIES)}`,
    pick(TAILS),
  ].join("\n\n");
}
