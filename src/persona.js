function normalize(text) {
  return String(text ?? "")
    .trim()
    .toLowerCase()
    .replace(/[!?.~,，。！？]/g, "");
}

function compact(text) {
  return normalize(text).replace(/\s+/g, "");
}

function includesAny(text, words) {
  return words.some((word) => text.includes(word));
}

function pick(items) {
  return items[Math.floor(Math.random() * items.length)];
}

function matchesWord(normal, squeezed, word) {
  const normalizedWord = normalize(word);
  const squeezedWord = compact(word);

  return normal.includes(normalizedWord) || squeezed.includes(squeezedWord);
}

function matchesWords(normal, squeezed, words) {
  return words.some((word) => matchesWord(normal, squeezed, word));
}

function dailyGroupById(id) {
  return DAILY_GROUPS.find((group) => group.id === id);
}

function isShortGreeting(text) {
  const value = compact(text);
  return value.length <= 5 && SHORT_GREETINGS.includes(value);
}

function detectProfanityHits(text) {
  const normal = normalize(text);
  const squeezed = compact(text);

  return PROFANITY_GROUPS.filter((group) =>
    group.words.some((word) => normal.includes(word) || squeezed.includes(word))
  );
}

function shouldTreatCrazyAsEmphasis(hits, text, dailyGroup) {
  if (!dailyGroup || hits.length !== 1 || hits[0].id !== "crazy") {
    return false;
  }

  const normal = normalize(text);
  const squeezed = compact(text);
  const explicitInsults = [
    "미친놈",
    "미친년",
    "미친새끼",
    "또라이",
    "돌아이",
    "정신나갔",
    "정신 나갔",
    "제정신",
  ];

  return !matchesWords(normal, squeezed, explicitInsults);
}

function detectDailyGroup(text) {
  const raw = String(text ?? "").trim().toLowerCase();
  const normal = normalize(text);
  const squeezed = compact(text);
  const isQuestion =
    raw.includes("?") ||
    raw.includes("？") ||
    raw.endsWith("니") ||
    raw.endsWith("냐");
  const isBotDirected = matchesWords(
    normal,
    squeezed,
    BOT_REFERENCE_WORDS
  );

  const userAteGroup = dailyGroupById("userAte");
  if (
    !isQuestion &&
    userAteGroup &&
    matchesWords(normal, squeezed, userAteGroup.words) &&
    !isBotDirected
  ) {
    return userAteGroup;
  }

  const userLocationGroup = dailyGroupById("userLocation");
  if (
    !isQuestion &&
    userLocationGroup &&
    matchesWords(normal, squeezed, userLocationGroup.words) &&
    !isBotDirected
  ) {
    return userLocationGroup;
  }

  const priorityGroup = DAILY_PRIORITY_IDS.map(dailyGroupById).find(
    (group) => group && matchesWords(normal, squeezed, group.words)
  );
  if (priorityGroup) {
    return priorityGroup;
  }

  return DAILY_GROUPS.find((group) =>
    matchesWords(normal, squeezed, group.words)
  );
}

function detectCategory(text) {
  const normal = normalize(text);
  const squeezed = compact(text);

  return Object.entries(CATEGORY_WORDS).find(([, words]) =>
    words.some((word) => normal.includes(word) || squeezed.includes(word))
  )?.[0];
}

function getDailyCueReply(groupId, text) {
  const normal = normalize(text);
  const squeezed = compact(text);
  const rule = DAILY_CUE_RULES.find(
    (item) =>
      item.groupId === groupId && matchesWords(normal, squeezed, item.words)
  );

  if (!rule) return null;

  return {
    body: pick(rule.replies),
    followup: pick(rule.followups),
  };
}

