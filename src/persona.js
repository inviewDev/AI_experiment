function makeDailyReply(group, rawText) {
  const cue = getDailyCueReply(group.id, rawText);
  const body = cue?.body ?? pick(group.replies);
  const followup = cue?.followup ?? pick(group.followups);

  return [body, followup].filter(Boolean).join("\n\n");
}

function makeCategoryReply(category) {
  const opener = pick(OPENERS);
  const body = pick(CATEGORY_REPLIES[category] ?? GENERIC_BODIES);
  const tail = pick(TAILS);

  return [`${opener} ${body}`, tail].filter(Boolean).join("\n\n");
}

